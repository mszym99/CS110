# This program is a math program that does the following
# Prompt the user for two non-zero integers
intOne = int(input("Please enter a non-zero integer: "))
intTwo = int(input("Please enter a non-zero integer: "))


# Display the sum of the chosen integers
print(intOne , "Plus" , intTwo , "Equals" , intOne + intTwo)

# Display the difference between the two integers
print(intTwo , "Minus" , intOne , "Equals" , intTwo - intOne)

# Display the product of the two integers
print(intOne , "Times" , intTwo ,"Equals" , intOne * intTwo)

# Display the first number raised to the power of the second number
print(intOne , "To the" , intTwo , "Equals" , intOne ** intTwo)

# Display the quotient of the two integers with a remainder
print(intOne , "Divided by" , intTwo , "Equals" , intOne / intTwo)






